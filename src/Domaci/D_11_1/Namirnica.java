package Domaci.D_11_1;

public class Namirnica {

    private String naziv;
    private int cena;

    public Namirnica(String naziv, int cena) {
        this.naziv = naziv;
        this.cena = cena;
    }

    public String getNaziv() {
        return naziv;
    }

    public int getCena() {
        return cena;
    }
}
